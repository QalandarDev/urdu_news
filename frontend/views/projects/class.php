<h4><strong>Wider Objective: </strong></h4>

<p>To create master program on computational linguistics for master students with linguistics and computational science background and to implement blended learning technologies on specialization courses under the initiatives of Uzbekistan and Kazakhstan universities.</p>

<p>&nbsp;</p>
<h4><strong>Specific Project Objectives: </strong></h4>

<ul>
	<li>To create and implement interdisciplinary master program in CL in 7 CA universities under careful analysis of existing programs.</li>
	<li>To implement blended learning technologies on specialization courses.</li>
	<li>To develop NLP tools with good support for the Uzbek and Kazakh languages.</li>
</ul>

<p>&nbsp;</p>
<h4><strong>Equipment for the project: </strong></h4>

<p>For the two fields of science, linguistics and computer science, there were included two printers and two scanners for the project documentation for all CA partners. Moreover, there are included four laptops for using in the annual and semester meetings. As master related with programming and NLP tools 10 all in one HP desktop computers are included. Also for the open online courses there should be server and accessories (UPS, Enclosure, Network equipment and tools). Computer Linguistics program needs projector and SMART Board. There is a need for camera Canon, Falcon Eyes Power Station, Microphone for getting the quality video rollers and online courses. Master program needs core books for the students and academic staff. In developing the NLP tools and studying the English books the Prompt Expert will help with the translation and idea of translation process.</p>


