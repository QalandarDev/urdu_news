<h4><strong>The role of UrSU in the CLASS project</strong></h4>

<ul>
	<li>- Developing an action plan in each six month;</li>
	<li>- Purchasing equipment for administrative and teaching issues in Uzbekistan;</li>
	<li>- Developing and approving curriculum of master program;</li>
	<li>- Developing syllabuses and content materials of the course distributed equally among the Uzbek partners;</li>
	<li>- Developing Open online courses;</li>
	<li>- Developing NLP tools;</li>
	<li>- Creating video rollers;</li>
	<li>- Developing sharing materials, e-books and methodological books on CL distributed equally among the Uzbek partners;</li>
	<li>- Reporting to SC;</li>
	<li>- Regional administrative project management;</li>
	<li>- Task distribution among the local project team;</li>
	<li>- Teaching and instructing CA academic staff in syntactic, morphological and semantic structure of the English language;</li>
	<li>- 5 academic staff participation in training on teaching CL with blended learning technology;</li>
	<li>- 5 academic staff participation in training on development methodologies for NLP tools;</li>
	<li>- Introducing developed master program to other HEIs;</li>
	<li>- Organizing student enrollment procedure;</li>
	<li>- Operational capacity: Skills and expertise of key staff involved in the project.</li>
</ul>
